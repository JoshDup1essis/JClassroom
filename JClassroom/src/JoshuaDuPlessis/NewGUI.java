    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
    pnlMenuTeacher.setVisible(false);
    pnlCreateClasses.setVisible(true);

    // Get logged in username
    String tempUsername = storage.getCurrentUsername();

    // Create an instance of the Account class
    Account loggedInAccount = new Account(tempUsername, "", null, 0, false);
    // Get the teacher ID
    int teacherId = storage.getTeacherIdByUsername(tempUsername);
    loggedInAccount.setTeacherId(teacherId);
    List<String> selectedStudentNames = jLClassList.getSelectedValuesList();
    // Check if any students are selected
    if (!selectedStudentNames.isEmpty()) {
        // Add the class to the database and assign it to the teacher or student
        String className = txtClassName.getText();
        String classSubject = txtClassSubject.getText();
        if (loggedInAccount.isTeacher()) {
            storage.insertClassroom(className, classSubject, teacherId);
        } else {
            storage.insertClassroom(className, classSubject, teacherId);
        }

        int classId = storage.getClassIdByName(className);

        // Retrieve the student IDs based on the selected student names
        List<Integer> studentIds = storage.getStudentIdsByNames(selectedStudentNames);
        storage.addToClassList(classId, studentIds);
        System.out.println(studentIds);
      
    } else {
        System.out.println("No students selected.");
    }


}

