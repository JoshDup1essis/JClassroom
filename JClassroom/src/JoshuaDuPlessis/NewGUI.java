    private void btnValidateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnValidateActionPerformed
        String username = txtUsername.getText();
        String password = txtPassword.getText();
        Date dateOfBirth = yrValue.getDate();
        int age = Integer.parseInt(spnAge.getValue().toString());
        boolean teacherSelected = btnAgreement.isSelected();

        boolean isValid = AccountValidator.validate(username, password, dateOfBirth, age, teacherSelected);

        if (isValid) {
            Account newAccount = new Account(username, password, dateOfBirth, age, teacherSelected);
           
            storage.insertAccount(newAccount);
            
            txtUsername.setText("");
            txtPassword.setText("");
            yrValue.setDate(null);
            spnAge.setValue(0);
            btnAgreement.setSelected(false);
            pnlRegister.setVisible(false);
            pnlHome.setVisible(true);
        }  else {
            // show error message
        }
    }//GEN-LAST:event_btnValidateActionPerformed

